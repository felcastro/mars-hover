package app;

import java.util.ArrayList;

public class Plateau {

	public int height, width;
	
	public Plateau(int height, int width){
		this.height = height;
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}
}
